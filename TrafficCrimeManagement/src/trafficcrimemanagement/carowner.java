    private void retrievedata(){
    try {
       
                String sql="Select * from carowner";
                rs=param.executeQuery();
                if(rs.next()){  
                     //retrieve the person detail
                        String name=rs.getString(1);
                        String carmodel=rs.getString(2);
                        String cnic=rs.getString(3);
                        String address=rs.getString(4);
                     //retrieve the car image from database and set to the label
                        byte[] img=rs.getBytes("CAR_IMAGE");
                        ImageIcon image=new ImageIcon(img);
                        Image im=image.getImage();
                        Image myimg=im.getScaledInstance(car_image.getWidth(),car_image.getHeight(),Image.SCALE_SMOOTH);
                        ImageIcon newimage=new ImageIcon(myimg);
                        car_image.setIcon(newimage);
                      //retrieve the person image from database and set to the label
                        byte[] per_img=rs.getBytes("PERSON_IMG");
                        ImageIcon per_image=new ImageIcon(per_img);
                        Image per_im=per_image.getImage();
                        Image perimg=per_im.getScaledInstance(car_image.getWidth(),car_image.getHeight(),Image.SCALE_SMOOTH);
                        ImageIcon personimage=new ImageIcon(perimg);
                        person_image.setIcon(personimage);
                      //display the person detail
                        name_tf.setText(name);
                        model_tf.setText(carmodel);
                        cnic_tf.setText(cnic);
                        address_tf.setText(address);
                       //car_image.setIcon(icon);
                  
                } } catch (SQLException ex) {
                Logger.getLogger(carowner.class.getName()).log(Level.SEVERE, null, ex);
            } 
 
    }
