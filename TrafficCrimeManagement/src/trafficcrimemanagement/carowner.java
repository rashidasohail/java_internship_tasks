    public boolean checkuser(String numplate){
          boolean user=false;   
          try {
                String sql="Select * from carowner where numberplate=?";
                param=conn.prepareStatement(sql);
                param.setString(1, numplate);
           
                rs=param.executeQuery();
                if(rs.next()){
                   user=true;      
                }
          } catch (SQLException ex) {
             Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
          }
          return user;
 
    }
